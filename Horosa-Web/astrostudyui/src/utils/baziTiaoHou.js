// 输入 four；返回 { school, version, climate, yong:[主用,辅...], primary }
export function computeTiaoHou(four){
	const dayGan = four && four.day && four.day.stem ? four.day.stem.cell : '';
	const monthZhi = four && four.month && four.month.branch ? four.month.branch.cell : '';
	const row = TIAOHOU[dayGan];
	const yong = row && row[monthZhi];
	if(!yong){ return null; }
	return {
		school: '调候派',
		version: '速查版',
		climate: CLIMATE[monthZhi] || '',
		monthZhi,
		yong: yong.slice(),
		primary: yong[0],
	};
}

export default computeTiaoHou;
