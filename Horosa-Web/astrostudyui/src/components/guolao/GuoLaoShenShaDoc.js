const JX_TAG = {'吉': '【吉】', '凶': '【凶】', '中': '【中】'};

// 单个神煞的悬浮判语；缺项兜底，不臆造。
export function guolaoShenShaTip(name){
	const key = `${name || ''}`.trim();
	if(!key){
		return '';
	}
	const doc = GUOLAO_SHENSHA_DOC[key];
	if(doc){
		return `${JX_TAG[doc.jx] || ''}${key}：${doc.desc}`;
	}
	return `${key}：神煞（释义待补）`;
}

export function guolaoShenShaJx(name){
	const doc = GUOLAO_SHENSHA_DOC[`${name || ''}`.trim()];
	return doc ? doc.jx : '';
}
