export function normalizeLiuRengJiangName(name){
	const raw = `${name || ''}`.trim();
	if(!raw){
		return '';
	}
	return JIANG_ALIASES[raw] || raw;
}

function getShenTitle(branch){
	const info = SHEN_INFO[branch];
	if(!info){
		return `${branch || '—'}神`;
	}
	return info.title || `${info.name || ''}${branch || ''}神`;
}

function getShenSymbol(branch){
	const info = SHEN_INFO[branch];
	return info && info.symbol ? info.symbol : '未载于《神》章。';
}

function normalizeBranch(branch){
	const txt = `${branch || ''}`;
	const match = txt.match(/[子丑寅卯辰巳午未申酉戌亥]/);
	return match ? match[0] : '';
}

export function buildLiuRengShenTipObj(branch){
	const shenBranch = normalizeBranch(branch);
	if(!shenBranch){
		return null;
	}
	const info = SHEN_INFO[shenBranch];
	const tips = [];
	if(info && info.month){
		tips.push(info.month);
	}
	if(info && info.verse1){
		tips.push(info.verse1);
	}
	if(info && info.verse2){
		tips.push(info.verse2);
	}
	if(info && info.desc){
		tips.push(info.desc);
	}
	if(info && info.symbol){
		tips.push(`类象：${info.symbol}`);
	}
	return {
		title: getShenTitle(shenBranch),
		tips: tips.length > 0 ? tips : [`神义：${getShenSymbol(shenBranch)}`],
	};
}

export function buildLiuRengHouseTipObj(jiangName, tianBranch, diBranch){
	const jiangRaw = `${jiangName || ''}`.trim();
	const jiang = normalizeLiuRengJiangName(jiangRaw);
	if(!jiang || !tianBranch || !diBranch){
		return null;
	}
	const jiangTextMap = JIANG_BRANCH_NOTE[jiang] || {};
	const tianLine = jiangTextMap[tianBranch] || '未载于《将》文。';
	const diLine = jiangTextMap[diBranch] || '未载于《将》文。';
	const title = jiangRaw || jiang;
	return {
		title,
		tips: [
			`天盘神：${getShenTitle(tianBranch)}`,
			`${tianBranch}——${tianLine}。`,
			'==',
			`地盘神：${getShenTitle(diBranch)}`,
			`${diBranch}——${diLine}。`,
		],
	};
}
