// 取干支形象短串：色·质（支＝生肖）。
export function ganXiangText(gan){
	const x = GAN_XIANG[gan];
	if(!x){
		return '';
	}
	return `${x.color}·${x.material}（支:${x.branch}=${ZHI_ZODIAC[x.branch] || ''}）`;
}

