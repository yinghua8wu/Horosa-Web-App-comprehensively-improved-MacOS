describe('日禽 §10.2 定局 60×7 全表 byte-match(锁 mansionOfDay + yuanJiang 一整 420 周期)', () => {
	test('一整 420 日循环逐日 = 定局表', () => {
		for (let diff = 0; diff < 420; diff++) {
			const { y, m, d } = dateFromDiff(diff);
			const g = diff % 60;       // 干支序(0=甲子)
			const e = Math.floor(diff / 60); // 元 0-indexed
			expect(mansionOfDay(y, m, d).name).toBe(DINGJU[g][e]);
			expect(yuanJiangOfDay(y, m, d).yuan).toBe(e + 1);
		}
	});
});

describe('日禽相位锁定 + 420周期 + 干支锚', () => {
	test('任意日"值宿之曜 = 当天星期",连续 20000 天 100% 成立', () => {
		for (let diff = 0; diff < 20000; diff++) {
			const { y, m, d } = dateFromDiff(diff);
			const mansion = mansionOfDay(y, m, d);
			const weekday = new Date((ANCHOR + diff) * 86400000).getUTCDay();
			expect(YAO_TO_WEEKDAY[mansion.yao]).toBe(weekday);
			expect(WEEKDAY_TO_YAO[weekday]).toBe(mansion.yao);
		}
	});
	test('420日周期:干支与值宿不变', () => {
		[0, 37, 211, 5000].forEach((diff) => {
			const a = dateFromDiff(diff); const b = dateFromDiff(diff + 420);
			expect(mansionIdxOfDay(a.y, a.m, a.d)).toBe(mansionIdxOfDay(b.y, b.m, b.d));
			expect(ganzhiOfDay(a.y, a.m, a.d)).toBe(ganzhiOfDay(b.y, b.m, b.d));
		});
	});
	test('锚点 1996-01-28 = 甲子 = 虚日鼠 = 周日 = 一元一将', () => {
		expect(ganzhiOfDay(1996, 1, 28)).toBe('甲子');
		expect(mansionOfDay(1996, 1, 28).name).toBe('虚日鼠');
		expect(new Date(ANCHOR * 86400000).getUTCDay()).toBe(0);
		expect(yuanJiangOfDay(1996, 1, 28)).toEqual({ yuan: 1, jiang: 1 });
		// 2002-02-10 己酉 = 二元四将 = 房日兔(大全§1.7 程序更正值)
		expect(ganzhiOfDay(2002, 2, 10)).toBe('己酉');
		expect(yuanJiangOfDay(2002, 2, 10)).toEqual({ yuan: 2, jiang: 4 });
		expect(mansionOfDay(2002, 2, 10).name).toBe('房日兔');
	});
});

