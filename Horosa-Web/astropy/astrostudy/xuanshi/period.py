_MACRO_INDEX = {m: (i, s, e) for i, (m, s, e) in enumerate(_MACRO_TABLE)}


def classify_period(period: Optional[str]) -> Optional[PeriodSpec]:
    """把 period 字符串归类。命中不上即 None。"""
    if not period:
        return None
    p = period.strip()
    for needle, macro in _MICRO_TO_MACRO:
        if needle in p:
            i, s, e = _MACRO_INDEX[macro]
            return PeriodSpec(macro=macro, sort_order=i, span_start=s, span_end=e)
    return None


def all_macros() -> list[tuple[str, int, int]]:
    """主朝代按时序返回 [(name, start, end)]。"""
    return list(_MACRO_TABLE)


def year_to_macro(year: Optional[int]) -> Optional[str]:
    """给定公历年份返回所在大朝代（区间含端点，命中第一个）。

    朝代区间有重叠（如十六国/南北朝/辽金），按 _MACRO_TABLE 顺序取首个命中。
    """
    if year is None:
        return None
    for name, start, end in _MACRO_TABLE:
        if start <= year <= end:
            return name
    return None


