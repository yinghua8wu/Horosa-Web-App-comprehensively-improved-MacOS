_DATE_RE = re.compile(r"^([一-鿿]{2,8}?)\s*(?:([元一二三四五六七八九十百\d]+)\s*年)?")


def date_phrase_to_year(phrase: Optional[str]) -> Optional[int]:
    """把 '武德元年十月壬申' / '贞观九年' 转成公历公元年。"""
    if not phrase:
        return None
    s = phrase.strip()
    # 试套年号
    for era, base in ERA_BEGINNINGS.items():
        if s.startswith(era):
            rest = s[len(era):]
            m = re.match(r"^\s*(元|[一二三四五六七八九十百]+|\d+)\s*年", rest)
            if not m:
                # 仅有年号（如"武德"）
                return base
            num = _han_to_int(m.group(1))
            if num is None:
                return base
            return base + num - 1
    return None


_HAN_DIGIT = {"〇": 0, "零": 0, "一": 1, "二": 2, "三": 3, "四": 4,
              "五": 5, "六": 6, "七": 7, "八": 8, "九": 9}


def _han_to_int(s: str) -> Optional[int]:
    if s == "元":
        return 1
    if s.isdigit():
        return int(s)
    # 处理 一/二/十/十一/二十/二十一
    if "百" in s:
        # 简单：百一 = 100+1 = 101；不常见，回退
        return None
    if "十" in s:
        parts = s.split("十")
        left, right = parts[0], parts[1] if len(parts) > 1 else ""
        tens = _HAN_DIGIT.get(left, 1 if left == "" else None)
        ones = _HAN_DIGIT.get(right, 0 if right == "" else None)
        if tens is None or ones is None:
            return None
        return tens * 10 + ones
    return _HAN_DIGIT.get(s)
