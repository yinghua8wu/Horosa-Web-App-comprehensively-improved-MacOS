# ── §6.8 五虎遁(命宫配干):正月寅宫天干序 ───────────────────────────
def wuhu_yin_stem_index(year_stem_index: int) -> int:
    """正月(寅宫)天干序 = (年干序×2+2) % 10。"""
    return (year_stem_index * 2 + 2) % 10


def life_palace_stem_index(year_stem_index: int, life_branch_index: int) -> int:
    """命宫配干 = 寅宫干 + (命宫支 − 寅支) 顺数。寅支序=2。"""
    yin = wuhu_yin_stem_index(year_stem_index)
    return (yin + (life_branch_index - 2 + 12) % 12) % 10


def dignity_of(star: str, branch: str) -> str:
    """查七政庙旺(star∈日月水金火木土, branch∈子..亥)。"""
    row = DIGNITY_TABLE.get(star)
    if not row:
        return "平"
    return row[DIZHI.index(branch)]


